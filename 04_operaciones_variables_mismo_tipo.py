# OPERACIONES CON VARIABLES DEL MISMO TIPO

# Cuando tenemos variables del mismo tipo es muy sencillo realizar operaciones, solo usando operadores matematicos


# Definimos dos variables float
luz = 1100.50
gas = 950.30
# Definimos una nueva variable que va a guardar la suma de las 2 anteriores
gasto_servicios = luz + gas
# Mostramos el resultado en pantalla
print (gasto_servicios)


# Definimos dos variables string
nombre = "Clara"
apellido = " Gonzalez"
# Definimos una variable para guardar el nombre completo
nombre_completo = nombre + apellido
# Mostramos en pantalla el nombre completo
print (nombre_completo)