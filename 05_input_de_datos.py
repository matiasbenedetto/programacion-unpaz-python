# INPUT DE DATOS

# Con la función input() podemos pedir al usuario del programa que ingrese un dato, y podemos guardar ese dato en una variable
# Los datos ingresados por el usuario siempre van a ser guardados en una variable de tipo string


# Guardamos en la variable precio el input del usuario
precio = input("Ingrese el precio de lista: ")
print (precio)


# Guardamos en la variable cantidad el input del usuario
cantidad = input("Ingrese la cantidad: ")
print (cantidad)


# Guardamos en la variable nombre el input del usuario
nombre = input("¿Cual es tu nombre? ")
print (nombre)